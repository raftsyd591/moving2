/**
 * استخراج محتوى المقال من ملف تفاعلي
 * @param {Object} module - وحدة مستوردة من ملف المقال
 * @returns {string} - محتوى المقال بصيغة HTML أو محتوى فارغ
 */
export const extractContentFromModule = (module) => {
  try {
    // إذا كان الوحدة دالة تعيد مكوناً
    if (module.default && typeof module.default === 'function') {
      // محاولة الحصول على المحتوى من خاصية المكون
      const component = module.default();
      if (component.props && component.props.content) {
        return component.props.content;
      }
      
      // محاولة العثور على محتوى في المكون
      if (component.props && component.props.children) {
        const children = component.props.children;
        if (Array.isArray(children)) {
          // البحث عن عنصر يحتوي على محتوى HTML
          const contentChild = children.find(child => 
            typeof child === 'object' && 
            child.props && 
            child.props.dangerouslySetInnerHTML
          );
          
          if (contentChild && contentChild.props.dangerouslySetInnerHTML.__html) {
            return contentChild.props.dangerouslySetInnerHTML.__html;
          }
        }
      }
    }
    
    // محاولة العثور على متغير محتوى معرف مباشرة في الوحدة
    if (module.content) {
      return module.content;
    }
    
    if (module.articleContent) {
      return module.articleContent;
    }
    
    // إعادة سلسلة فارغة إذا لم يتم العثور على محتوى
    return '';
  } catch (error) {
    console.error('Error extracting content from module:', error);
    return '';
  }
};

